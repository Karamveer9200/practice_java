package C2_OOPS_and_Basics;



public class ch1_variables_01 {

    // 'final' keyword is used to make a variable constant (it cannot be changed later)
    final static int MINUTES_IN_A_DAY = 1440;
    final static int MINUTES_IN_AN_HOUR = 60;

    public static void main(String[] args) {
        System.out.println(MINUTES_IN_A_DAY);
        System.out.println(MINUTES_IN_AN_HOUR);
    }
}
